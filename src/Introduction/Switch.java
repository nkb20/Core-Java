package Introduction;

public class Switch {
    public static void main(String[] args){

        byte a=5;
        switch(a){
            case 1:
            {
                System.out.println("Case is 1");
                break;

            }
            case 2:
            {
                System.out.println("Case is 2");
break;
            }
            case 3:
            {
                System.out.println("Case is 3");
                break;

            }
            default:{
                System.out.println("No case matched");

            }

        }

    }
}
